//HTML
//Conceptos Clave:
//HTML: Lenguaje de marcado que se utiliza para estructurar contenido en la web.

//Etiquetas comunes:
// <html>: Indica el inicio y el final de un documento HTML.
// <head>: Contiene metadatos sobre el documento (título, enlaces a CSS, scripts).
// <title>: Define el título del documento, que se muestra en la pestaña del navegador.
// <meta>: Proporciona metadatos como la codificación de caracteres.
// <link>: Vincula hojas de estilo externas (CSS).
// <script>: Incluye scripts de JavaScript.
// <body>: Contiene el contenido visible de la página.
// <h1> a <h6>: Definen encabezados (h1 es el más importante y el de mayor tamaño).
// <p>: Define un párrafo de texto.
// <a>: Define un hipervínculo.
// <img>: Inserta imágenes.
// <ul> y <ol>: Crea listas (desordenadas y ordenadas).
// <div>: Contenedor genérico para agrupar elementos.
// <span>: Contenedor en línea para aplicar estilos.
// <form>: Crea formularios para enviar datos.
// <input>: Define campos de entrada en un formulario.

