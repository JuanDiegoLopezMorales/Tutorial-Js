//Ciclos o Bucles en JavaScript
//Conceptos Clave:
//for: Repite un bloque de código un número específico de veces.

//Sintaxis:
//for (inicialización; condición; incremento) {  
// código a ejecutar}

//while: Repite un bloque de código mientras la condición sea verdadera.

//Sintaxis:
//while (condición) {
  // código a ejecutar}
  
//do...while: Similar a while, pero el bloque se ejecuta al menos una vez.

//Sintaxis:
//do {
// código a ejecutar
//  } while (condición)

//Preguntas:
//¿Cuándo usarías un bucle for en lugar de un bucle while?
//¿Qué pasaría si la condición de un bucle while nunca se vuelve falsa?

//Tarea Práctica:
//Crea un bucle que imprima los números del 1 al 5.

for (let i = 1; i <= 5; i++) { // Bucle for que comienza en 1 y termina en 5
    console.log(i); // Imprime el valor actual de i
  }